<script>
    document.addEventListener('DOMContentLoaded', () => {
    const container = document.querySelector('.block-card-inf');
    const prevBtn = document.querySelector('.prev-reviews-btn');
    const nextBtn = document.querySelector('.next-reviews-btn');

    if (!container) return;

    const getStepWidth = () => {
        const item = container.firstElementChild;
        const gap = parseInt(window.getComputedStyle(container).columnGap) || 0;
        return item ? item.offsetWidth + gap : 0;
    };

    requestAnimationFrame(() => {
        container.scrollLeft = container.scrollWidth;
    });

    const scrollToIndex = (direction) => {
        const step = getStepWidth();
        const currentStep = Math.round(container.scrollLeft / step);
        
        const targetLeft = (currentStep + direction) * step;

        container.scrollTo({
            left: targetLeft,
            behavior: 'smooth'
        });
    };

    nextBtn?.addEventListener('click', () => scrollToIndex(1));
    prevBtn?.addEventListener('click', () => scrollToIndex(-1));
});
</script>

<script>
document.addEventListener('DOMContentLoaded', () => {
    const navLinks = document.querySelectorAll('.flex.flex-col a[href*="document.getElementById"]');

    const activeBgClass = 'bg-[#EEEEEE]';
    const activeTextClass = `[font-variation-settings:'wght'_700]`;
    const activeStrokeClass = 'stroke-[2px]';

    const options = {
        root: null,
        rootMargin: '-181px 0px -50% 0px',
        threshold: 0
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const activeId = entry.target.id;
                
                navLinks.forEach(link => {
                    const isTarget = link.getAttribute('href').includes(`'${activeId}'`);
                    const svgPaths = link.querySelectorAll('svg path');
                    const textSpan = link.querySelector('span:last-child');

                    if (isTarget) {
                        
                        link.classList.add(activeBgClass);
                        textSpan?.classList.add(activeTextClass);
                        svgPaths.forEach(path => path.classList.add(activeStrokeClass));
                    } else {
                        
                        link.classList.remove(activeBgClass);
                        textSpan?.classList.remove(activeTextClass);
                        svgPaths.forEach(path => path.classList.remove(activeStrokeClass));
                    }
                });
            }
        });
    }, options);

    navLinks.forEach(link => {
        const match = link.getAttribute('href').match(/'([^']+)'/);
        if (match && match[1]) {
            const section = document.getElementById(match[1]);
            if (section) observer.observe(section);
        }
    });
});
