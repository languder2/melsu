<script>
    document.addEventListener('DOMContentLoaded',()=>{

        const linkElement = document.querySelector('a[href="http://melsu.main/regiments"]');

        if (linkElement) {
            const parentBlock = linkElement.closest('.hidden.lg\\:block.z-20.top-28.right-2.fixed.transition-all.duration-400');
            
            if (parentBlock) {
                parentBlock.style.display = 'none';
            }
        }

        let nextFirstBtn = document.querySelector('.first-next');
        let prevFirstBtn = document.querySelector('.first-prev');

        let firstList = document.querySelectorAll('.first-info-box');

        let firstCount = document.querySelector('.first-lenght');
        let firstCurrentCount = document.querySelector('.first-current');

        firstCount.textContent = firstList.length.toString().padStart(2, '0');

        let secondList = document.querySelectorAll('.second-info-box');

        let nextSecondBtn = document.querySelector('.second-next');
        let prevSecondBtn = document.querySelector('.second-prev');

        let secondCount = document.querySelector('.second-lenght');
        let secondCurrentCount = document.querySelector('.second-current');

        secondCount.textContent = secondList.length.toString().padStart(2, '0');

        let currentFirstIndex = 0;
        let currentSecondIndex = 0;

        function updateCurrentCount(currentIndex, currentCount) {
            const displayIndex = currentIndex + 1;
            currentCount.textContent = displayIndex.toString().padStart(2, '0');
        }

        function showCurrentFaculty(List, currentIndex, currentCount) {
            List.forEach((faculty,index) => {
                faculty.classList.add('hidden');
            });

            if (List[currentIndex]) {
                List[currentIndex].classList.add('flex');
                List[currentIndex].classList.remove('hidden');

            }

            updateButtonState(List, currentIndex);
            updateCurrentCount(currentIndex, currentCount);
        }

        function updateButtonState(List, currentIndex) {
            prevFirstBtn.disabled = currentIndex === 0;

            nextFirstBtn.disabled = currentIndex === List.length - 1;
        }


        nextFirstBtn.addEventListener('click', () => {
            if (currentFirstIndex < firstList.length - 1) {
                currentFirstIndex++;
                showCurrentFaculty(firstList, currentFirstIndex, firstCurrentCount);
                
            }
        });

        prevFirstBtn.addEventListener('click', () => {
            if (currentFirstIndex > 0) {
                currentFirstIndex--;
                showCurrentFaculty(firstList, currentFirstIndex, firstCurrentCount);
            }
        });

        if (firstList.length > 0) {
            showCurrentFaculty(firstList, currentFirstIndex, firstCurrentCount);
        }

        nextSecondBtn.addEventListener('click', () => {
            if (currentSecondIndex < secondList.length - 1) {
                currentSecondIndex++;
                showCurrentFaculty(secondList, currentSecondIndex, secondCurrentCount);
                
            }
        });

        prevSecondBtn.addEventListener('click', () => {
            if (currentSecondIndex > 0) {
                currentSecondIndex--;
                showCurrentFaculty(secondList, currentSecondIndex, secondCurrentCount);
            }
        });

        if (secondList.length > 0) {
            showCurrentFaculty(secondList, currentSecondIndex, secondCurrentCount);
        }

        const targetSections = document.querySelectorAll('#welcome, #important-dates, #first-steps, #dormitories, #first-year-memo, #contact-details, #video-guide, #news, #sport, #rules, #payment-information, #medical-care');
        
        const activeClass = 'bg-[#303030]';

        const setActiveLink = (sectionId) => {
            const allLinks = document.querySelectorAll('.card-news-glass a');
            
            allLinks.forEach(link => {
                link.classList.remove(activeClass);
                
            });

            if (sectionId) {
                const activeLinks = document.querySelectorAll(
                    `.card-news-glass a[href*="document.getElementById('${sectionId}')"]`
                );
                
                activeLinks.forEach(link => {
                    link.classList.add(activeClass);
                });
            }
        };

        const observerOptions = {
            root: null,
            rootMargin: '0px 0px -50% 0px',
            threshold: 0
        };

        const observer = new IntersectionObserver((entries, observer) => {
            let activeSectionId = null;

            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    activeSectionId = entry.target.id;
                }
            });

            if (activeSectionId) {
                setActiveLink(activeSectionId);
            }
            
        }, observerOptions);
        targetSections.forEach(section => {
            observer.observe(section);
        });
    })
</script>

