<script>
    document.addEventListener('DOMContentLoaded', function () {
        const scrollContainer = document.querySelector('.block-scroll');
        const scrollBar = document.querySelector('.vertical-scrollbar');
        const scrollHandle = document.querySelector('.scroll-handle');

        let isDragging = false;
        let startY = 0;
        let startTop = 0;

        function updateScrollHandle() {
            const { scrollTop, scrollHeight, clientHeight } = scrollContainer;
            const handleHeight = Math.max(20, (clientHeight / scrollHeight) * clientHeight);
            const maxScrollTop = scrollBar.clientHeight - handleHeight;
            const scrollPercent = scrollTop / (scrollHeight - clientHeight);

            scrollHandle.style.height = `${handleHeight}px`;
            scrollHandle.style.top = `${scrollPercent * maxScrollTop}px`;
        }

        scrollContainer.addEventListener('scroll', updateScrollHandle);
        window.addEventListener('resize', updateScrollHandle);

        scrollHandle.addEventListener('mousedown', (e) => {
            isDragging = true;
            startY = e.clientY;
            startTop = parseInt(scrollHandle.style.top) || 0;
        });

        document.addEventListener('mousemove', (e) => {
            if (!isDragging) return;
            const deltaY = e.clientY - startY;
            const newTop = Math.min(Math.max(0, startTop + deltaY), scrollBar.clientHeight - scrollHandle.offsetHeight);
            const scrollHeight = scrollContainer.scrollHeight;
            const clientHeight = scrollContainer.clientHeight;

            scrollHandle.style.top = `${newTop}px`;

            scrollContainer.scrollTop = (newTop / (scrollBar.clientHeight - scrollHandle.offsetHeight)) * (scrollHeight - clientHeight);
        });

        document.addEventListener('mouseup', () => {
            isDragging = false;
            scrollHandle.style.cursor = 'grab';
        });

        scrollBar.addEventListener('click', (e) => {
            const rect = scrollBar.getBoundingClientRect();
            const clickY = e.clientY - rect.top;
            const handleHeight = scrollHandle.offsetHeight;
            const targetTop = clickY - handleHeight / 2;

            const clampedTop = Math.min(Math.max(0, targetTop), scrollBar.clientHeight - handleHeight);
            scrollHandle.style.top = `${clampedTop}px`;

            const scrollHeight = scrollContainer.scrollHeight;
            const clientHeight = scrollContainer.clientHeight;
            scrollContainer.scrollTop = (clampedTop / (scrollBar.clientHeight - handleHeight)) * (scrollHeight - clientHeight);
        });

        updateScrollHandle();
    });
</script>
