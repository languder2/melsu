<p class="mb-3 bg-white p-6">
    Мелитопольский государственный университет начинает свою историю с 1874 года, когда было создано Реальное училище города Мелитополя.
    После революции на базе бывшего Реального училища был создан техникум, который начал готовить специалистов по работе с сельскохозяйственной техникой.<br><br>
    В 1932 году на базе техникума индустриализации сельского хозяйства был создан технический вуз-завод ВТУЗ им. ОГПУ. В 1934—1935 годах во ВТУЗе работало 13 кафедр и обучалось около 500 студентов.
</p>
