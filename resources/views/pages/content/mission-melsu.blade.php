<script>
    document.addEventListener('DOMContentLoaded', function () {
        document.querySelector('.my-6').classList.add('hidden');
        const btnNext = document.querySelector('.next-btn');
        const btnPrev = document.querySelector('.prev-btn');
        const container = document.querySelector('.flex.flex-col.gap-3');

        const modalContent = document.querySelectorAll('.modal-image');
        const openModalBtn = document.querySelectorAll('.modal-btn');
        const closeBtn = document.querySelectorAll('.close-modal');

        function getVisibleItems() {
            return Array.from(container.children).filter(item => !item.classList.contains('hidden'));
        }

        function updateClasses() {
            const items = getVisibleItems();

            items.forEach(item => item.classList.remove('active', 'next', 'last'));

            if (items[0]) items[0].classList.add('active');
            if (items[1]) items[1].classList.add('next');
            if (items[2]) items[2].classList.add('last');

            const currentActive = items[0];
            const targetId = currentActive?.getAttribute('data-target');

            const allTexts = document.querySelectorAll('.text-container');
            allTexts.forEach(text => {
                if (text.id === targetId) {
                    text.classList.remove('hidden', 'slide-up');
                } else {
                    text.classList.add('slide-up');
                    setTimeout(() => {
                        text.classList.add('hidden');
                    }, 600);
                }
            });
        }

        btnNext.addEventListener('click', () => {
            const visibleItems = getVisibleItems();
            const first = visibleItems[0];

            if (!first) return;

            first.classList.add('animate-up');

            setTimeout(() => {
                first.classList.remove('animate-up');
                first.classList.add('hidden');
                container.appendChild(first);

                const nextHidden = Array.from(container.children).find(item => item.classList.contains('hidden'));
                if (nextHidden) nextHidden.classList.remove('hidden');

                updateClasses();
            }, 600);
        });

        btnPrev.addEventListener('click', () => {
            const allItems = Array.from(container.children);
            const visibleItems = getVisibleItems();
            const firstVisible = visibleItems[0];

            if (!firstVisible) return;

            const currentIndex = allItems.findIndex(item => item === firstVisible);
            let prevIndex = (currentIndex - 1 + allItems.length) % allItems.length;

            const newItem = allItems[prevIndex];

            if (!newItem) return;

            newItem.classList.remove('animate-down');

            void newItem.offsetWidth;

            container.insertBefore(newItem, container.firstElementChild);

            newItem.classList.remove('hidden');
            newItem.classList.add('animate-down');

            setTimeout(() => {
                newItem.classList.remove('animate-down');
            }, 50);

            const lastVisible = visibleItems[2];
            if (lastVisible) {
                lastVisible.classList.add('hidden');
            }

            updateClasses();
        });

        updateClasses();



        closeBtn.forEach((item,index) => {
            item.addEventListener('click', () =>  {
                modalContent[index].hidePopover();
            });
        })

        document.addEventListener('click', (event) => {

            modalContent.forEach((item, index) =>{
                if (item.matches(':popover-open')) {
                    const clickInsideModal = event.composedPath().includes(item);
                    if (!clickInsideModal) {
                        item.hidePopover();
                    }
                }
            })

        });

    });
</script>
